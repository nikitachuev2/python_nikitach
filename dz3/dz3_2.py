
def is_prime(n):
    """
    Функция, которая принимает число n и возвращает True, если число простое, и False в противном случае.
    """
    # Если число меньше 2, оно не является простым
    if n < 2:
        return False
    
    # Проверяем, делится ли число на любое число от 2 до его квадратного корня
    for i in range(2, int(n ** 0.5) + 1):
        if n % i == 0:
            return False
    
    # Если число не делится ни на одно число от 2 до его квадратного корня, оно является простым
    return True

# Получаем число от пользователя
user_input = int(input("Введите число: "))

# Проверяем, является ли число простым
if is_prime(user_input):
    print(f"Число {user_input} является простым.")
else:
    print(f"Число {user_input} не является простым.")
